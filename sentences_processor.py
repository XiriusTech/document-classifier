def stem_tokens(tokens, stemmer):
  stemmed = []
  for item in tokens:
    stemmed.append(stemmer.stem(item))
  return stemmed

def calculate_grams(tokens, n):
  result = ""
  allgrams = ngrams(tokens, n)
  for grams in allgrams:
    sent_str = ""
    for i in grams:
      sent_str += str(i) + "_"
    sent_str = sent_str.rstrip('_')
    result += " " + sent_str
  return result.lstrip('')

